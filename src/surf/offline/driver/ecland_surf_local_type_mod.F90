  SUBROUTINE SURF_AND_MORE_LOCAL_TYPE_INIT(SELF,NPROMA, NBLOCKS, NLEV, NTILES, NTSW, NLWEMISS,NVHILO)
    CLASS(SURF_AND_MORE_LOCAL_TYPE) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: NPROMA, NLEV,  NBLOCKS, NTSW,NTILES, NLWEMISS,NVHILO

    CALL FIELD_NEW(SELF%F_PWL, DATA=WRENU0)
    CALL FIELD_NEW(SELF%F_PSNS, DATA=FSNNU0)
    CALL FIELD_NEW(SELF%F_PRSN, DATA=RSNNU0)
    CALL FIELD_NEW(SELF%F_PWSN, DATA=WSNNU0)
    CALL FIELD_NEW(SELF%F_PANDAYVT, DATA=VDANDAYVT)
    CALL FIELD_NEW(SELF%F_PANFMVT, DATA=VDANFMVT)
    CALL FIELD_NEW(SELF%F_PLAIHI, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PLAILI, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZLAIL, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZLAIH, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PZIDLWV, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZTHKICE, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZWLMX, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZCSN, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZCVT, UBOUNDS=[NPROMA,NVHILO,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZLAIVT, UBOUNDS=[NPROMA,NVHILO,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZKH, UBOUNDS=[NPROMA,NLEV,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZEXDIAG, UBOUNDS=[NPROMA,12,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZSPECTRALEMISS, UBOUNDS=[NPROMA,NLWEMISS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZEVAPSNW, UBOUNDS=[NPROMA,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_ZFRTI, UBOUNDS=[NPROMA,NTILES,NBLOCKS], PERSISTENT=.TRUE.)
