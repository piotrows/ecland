  SUBROUTINE SURF_AND_MORE_LOCAL_TYPE_UPDATE_VIEW(SELF, BLOCK_INDEX)
    CLASS(SURF_AND_MORE_LOCAL_TYPE) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: BLOCK_INDEX
    IF(ASSOCIATED(SELF%F_PLAILI))        SELF%PLAILI        => SELF%F_PLAILI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PLAIHI))        SELF%PLAIHI        => SELF%F_PLAIHI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZLAIL))         SELF%ZLAIL         => SELF%F_ZLAIL%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZLAIH))         SELF%ZLAIH         => SELF%F_ZLAIH%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PZIDLWV))       SELF%PZIDLWV       => SELF%F_PZIDLWV%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZTHKICE))       SELF%ZTHKICE       => SELF%F_ZTHKICE%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZWLMX))         SELF%ZWLMX         => SELF%F_ZWLMX%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZCSN))          SELF%ZCSN          => SELF%F_ZCSN%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZCVT))          SELF%ZCVT          => SELF%F_ZCVT%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZLAIVT))        SELF%ZLAIVT        => SELF%F_ZLAIVT%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZKH))           SELF%ZKH           => SELF%F_ZKH%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZEXDIAG))       SELF%ZEXDIAG       => SELF%F_ZEXDIAG%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZSPECTRALEMISS)) SELF%ZSPECTRALEMISS => SELF%F_ZSPECTRALEMISS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZEVAPSNW))      SELF%ZEVAPSNW      => SELF%F_ZEVAPSNW%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZFRTI))         SELF%ZFRTI         => SELF%F_ZFRTI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZALBTI))        SELF%ZALBTI        => SELF%F_ZALBTI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZFRSOTI))       SELF%ZFRSOTI       => SELF%F_ZFRSOTI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PSLRFLTI))      SELF%PSLRFLTI      => SELF%F_PSLRFLTI%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZALBD))         SELF%ZALBD         => SELF%F_ZALBD%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_ZALBP))         SELF%ZALBP         => SELF%F_ZALBP%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PWL))           SELF%PWL           => SELF%F_PWL%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PSNS))          SELF%PSNS          => SELF%F_PSNS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PRSN))          SELF%PRSN          => SELF%F_PRSN%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PWSN))          SELF%PWSN          => SELF%F_PWSN%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PANDAYVT))      SELF%PANDAYVT      => SELF%F_PANDAYVT%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PANFMVT))       SELF%PANFMVT       => SELF%F_PANFMVT%GET_VIEW(BLOCK_INDEX)
