SUBROUTINE UPDDIAG( &
 &  IBL &
 & ,KIDIA,KFDIA,KLON,KLEV,KLEVS,KLEVSN,KTILES &
 & ,PTSPHY &
 & ,PPSURF &
 & ,PSURF, PSURFL, PAUX, PAUXL, PCLIM, PFLUX, PDIAG, STATE, GEMSL, PDDHS)

