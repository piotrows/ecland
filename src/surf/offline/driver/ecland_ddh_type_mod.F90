USE PARKIND1,                  ONLY : JPIM, JPRB
USE YOMCDH1S ,                 ONLY : NLEVI  , &
     &  NDHVTLS,NDHFTLS,NDHVTSS,NDHFTSS, &
     &  NDHVTTS,NDHFTTS,NDHVTIS,NDHFTIS, &
     &  NDHVSSS,NDHFSSS,NDHVIIS,NDHFIIS, &
     &  NDHVWLS,NDHFWLS,NDHVRESS,NDHFRESS, &
     &  NDHVCO2S,NDHFCO2S, &
     &  NDHVBIOS,NDHFBIOS,NDHVBVOCS,NDHVVEGS,NDHFVEGS
USE FIELD_MODULE, ONLY: FIELD_2RB, FIELD_3RB, FIELD_4RB
USE FIELD_FACTORY_MODULE

IMPLICIT NONE

PRIVATE

TYPE, PUBLIC :: DDH_SURF_TYPE
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHTLS   => NULL() ! Diagnostic array for tiles (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHTLS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHTSS   => NULL() ! Diagnostic array for snow T (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHTSS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHTTS   => NULL() ! Diagnostic array for soil T (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHTTS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHTIS   => NULL() ! Diagnostic array for ice T (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHTIS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHCO2S  => NULL()
  CLASS(FIELD_4RB), POINTER :: F_PDHCO2S  => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHBVOCS => NULL()
  CLASS(FIELD_4RB), POINTER :: F_PDHBVOCS => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHBIOS  => NULL()
  CLASS(FIELD_4RB), POINTER :: F_PDHBIOS  => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHVEGS  => NULL()
  CLASS(FIELD_4RB), POINTER :: F_PDHVEGS  => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHSSS   => NULL() ! Diagnostic array for snow mass (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHSSS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:), CONTIGUOUS,     POINTER :: PDHIIS   => NULL() ! Diagnostic array for interception layer (see module yomcdh)
  CLASS(FIELD_3RB), POINTER :: F_PDHIIS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:), CONTIGUOUS,   POINTER :: PDHWLS   => NULL() ! Diagnostic array for soil water (see module yomcdh)
  CLASS(FIELD_4RB), POINTER :: F_PDHWLS   => NULL()
  REAL(KIND=JPRB), DIMENSION(:), CONTIGUOUS,       POINTER :: PDHFSS   => NULL()

CONTAINS
  PROCEDURE :: INIT  => DDH_SURF_TYPE_INIT
  PROCEDURE :: FINAL => DDH_SURF_TYPE_FINAL
  PROCEDURE :: UPDATE_VIEW => DDH_SURF_TYPE_UPDATE_VIEW
  PROCEDURE :: SYNC_HOST   => DDH_SURF_TYPE_SYNC_HOST
END TYPE DDH_SURF_TYPE

CONTAINS

  SUBROUTINE DDH_SURF_TYPE_INIT(SELF, NPROMA, NBLOCKS, NCSS, NCSNEC, NTILES, NVHILO )
    CLASS(DDH_SURF_TYPE) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: NPROMA, NBLOCKS, NCSS, NCSNEC, NTILES, NVHILO
    CALL FIELD_NEW(SELF%F_PDHBVOCS, UBOUNDS=[NPROMA,NVHILO,NDHVBVOCS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHTLS, UBOUNDS=[NPROMA,NTILES,NDHVTLS+NDHFTLS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHTSS, UBOUNDS=[NPROMA,NCSNEC,NDHVTSS+NDHFTSS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHSSS, UBOUNDS=[NPROMA,NCSNEC,NDHVSSS+NDHFSSS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHTTS, UBOUNDS=[NPROMA,NCSS  ,NDHVTTS+NDHFTTS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHWLS, UBOUNDS=[NPROMA,NCSS  ,NDHVWLS+NDHFWLS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHTIS, UBOUNDS=[NPROMA,NLEVI ,NDHVTIS+NDHFTIS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHIIS, UBOUNDS=[NPROMA,       NDHVIIS+NDHFIIS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHCO2S, UBOUNDS=[NPROMA,NVHILO,NDHVCO2S+NDHFCO2S,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHBIOS, UBOUNDS=[NPROMA,NVHILO,NDHVBIOS+NDHFBIOS,NBLOCKS], PERSISTENT=.TRUE.)
    CALL FIELD_NEW(SELF%F_PDHVEGS, UBOUNDS=[NPROMA,NVHILO,NDHVVEGS+NDHFVEGS,NBLOCKS], PERSISTENT=.TRUE.)

    BLOCK
      INTEGER(KIND=JPIM) :: JIBL
      REAL(KIND=JPRB), POINTER :: ZPDHTTS(:,:,:)
      REAL(KIND=JPRB), POINTER :: ZPDHVEGS(:,:,:)
      DO JIBL = 1, NBLOCKS
        ZPDHTTS  => SELF%F_PDHTTS%GET_VIEW(JIBL)
        ZPDHVEGS => SELF%F_PDHVEGS%GET_VIEW(JIBL)
        ZPDHTTS  = 0._JPRB
        ZPDHVEGS = 0._JPRB
      ENDDO
    END BLOCK
  END SUBROUTINE DDH_SURF_TYPE_INIT

  SUBROUTINE DDH_SURF_TYPE_FINAL(SELF)
    CLASS(DDH_SURF_TYPE) :: SELF

    CALL FIELD_DELETE(SELF%F_PDHTLS)
    NULLIFY( SELF%PDHTLS)
    CALL FIELD_DELETE(SELF%F_PDHTSS)
    NULLIFY( SELF%PDHTSS)
    CALL FIELD_DELETE(SELF%F_PDHSSS)
    NULLIFY( SELF%PDHSSS)
    CALL FIELD_DELETE(SELF%F_PDHTTS)
    NULLIFY( SELF%PDHTTS)
    CALL FIELD_DELETE(SELF%F_PDHWLS)
    NULLIFY( SELF%PDHWLS)
    CALL FIELD_DELETE(SELF%F_PDHTIS)
    NULLIFY( SELF%PDHTIS)
    CALL FIELD_DELETE(SELF%F_PDHIIS)
    NULLIFY( SELF%PDHIIS)
    CALL FIELD_DELETE(SELF%F_PDHCO2S)
    NULLIFY( SELF%PDHCO2S)
    CALL FIELD_DELETE(SELF%F_PDHBIOS)
    NULLIFY( SELF%PDHBIOS)
    CALL FIELD_DELETE(SELF%F_PDHBVOCS)
    NULLIFY( SELF%PDHBVOCS)
    CALL FIELD_DELETE(SELF%F_PDHVEGS)
    NULLIFY( SELF%PDHVEGS)
  END SUBROUTINE DDH_SURF_TYPE_FINAL

  SUBROUTINE DDH_SURF_TYPE_UPDATE_VIEW(SELF, BLOCK_INDEX)
    CLASS(DDH_SURF_TYPE) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: BLOCK_INDEX
    IF(ASSOCIATED(SELF%F_PDHTLS))   SELF%PDHTLS   => SELF%F_PDHTLS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHTSS))   SELF%PDHTSS   => SELF%F_PDHTSS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHTTS))   SELF%PDHTTS   => SELF%F_PDHTTS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHTIS))   SELF%PDHTIS   => SELF%F_PDHTIS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHCO2S))  SELF%PDHCO2S  => SELF%F_PDHCO2S%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHBVOCS)) SELF%PDHBVOCS => SELF%F_PDHBVOCS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHBIOS))  SELF%PDHBIOS  => SELF%F_PDHBIOS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHVEGS))  SELF%PDHVEGS  => SELF%F_PDHVEGS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHSSS))   SELF%PDHSSS   => SELF%F_PDHSSS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHIIS))   SELF%PDHIIS   => SELF%F_PDHIIS%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PDHWLS))   SELF%PDHWLS   => SELF%F_PDHWLS%GET_VIEW(BLOCK_INDEX)
  END SUBROUTINE DDH_SURF_TYPE_UPDATE_VIEW

  SUBROUTINE DDH_SURF_TYPE_SYNC_HOST(SELF)
    CLASS(DDH_SURF_TYPE) :: SELF
    CALL SELF%F_PDHTLS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHTSS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHTTS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHTIS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHCO2S%SYNC_HOST_RDWR()
    CALL SELF%F_PDHBVOCS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHBIOS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHVEGS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHSSS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHIIS%SYNC_HOST_RDWR()
    CALL SELF%F_PDHWLS%SYNC_HOST_RDWR()
  END SUBROUTINE DDH_SURF_TYPE_SYNC_HOST

END MODULE ECLAND_DDH_TYPE_MOD
