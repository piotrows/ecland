SUBROUTINE UPDDIAG_LAYER_OPENMP( &
 & KIDIA,KFDIA,KLON,KLEV,KLEVS,KLEVSN,KTILES &
 & ,PTSPHY &
 & ,PPSURF &
 & ,PSURF, PSURFL, PAUX, PAUXL, PFLUX, PDIAG, STATE, GEMSL, PDDHS)

