!**** UPDDIAG - INTERFACE ROUTINE TO COPY IFS VARIABLES TO
!		OFFLINE CODE DIAGNOSTICS BUFFERS

!     S. Boussetta/G.Balsamo May 2010 Add CTESSEL based on:
!        Marita Voogt (KNMI) "C-Tessel" 09/2005
!        Sebastien Lafont (ECMWF) "C-TESSEL"

!     E. Dutra May 2014, add KIDIAF,KFDIAF for NPROMA implementation 

!
! INTERFACE
!
!    *KLON*         NUMBER OF GRID POINTS PER PACKET
!    *KIDIA*        FIRST POINT
!    *KFDIA*        LAST POINT
!    *KLEV*         NUMBER OF LOWEST LEVEL
!    *KLEVS*        SOIL LAYERS
!    *KLEVSN*       SNOW LAYERS
!    *KTILES*       NUMBER OF TILES (I.E. SUBGRID AREAS WITH DIFFERENT 
!                   OF SURFACE BOUNDARY CONDITION)

!    *KVTYPES*      NUMBER OF VEGETATION TYPES

!    *KDHVTLS*      Number of variables for individual tiles
!    *KDHFTLS*      Number of fluxes for individual tiles
!    *KDHVTSS*      Number of variables for snow energy budget
!    *KDHFTSS*      Number of fluxes for snow energy budget
!    *KDHVTTS*      Number of variables for soil energy budget
!    *KDHFTTS*      Number of fluxes for soil energy budget
!    *KDHVIIS*      Number of variables for interception water budget
!    *KDHFIIS*      Number of fluxes for interception water budget
!    *KDHVSSS*      Number of variables for snow water budget
!    *KDHFSSS*      Number of fluxes for snow water budget
!    *KDHVWLS*      Number of variables for soil water budget
!    *KDHFWLS*      Number of fluxes for soil water budget
!    *KDHVRESS*     Number of variables for resistances 
!    *KDHFRESS*     Number of fluxes for resistances

!    *KDHVCO2S*     Number of variables for CO2
!    *KDHFCO2S*     Number of fluxes for CO2
!    *KDHVBIOS*     Number of variables for biomass
!    *KDHFBIOS*     Number of fluxes for biomass
!    *KDHVVEGS*     Number of variables for vegetation
!    *KDHFVEGS*     Number of fluxes for vegetation


!
!    *PTSPHY*       time step length
!    *PDHTLS*	    surface fluxes
!    *PDHTSS*	    snow fluxes
!    *PDHTTS*	    soil T fluxes
!    *PDHSSS*	    snow mass fluxes
!    *PDHIIS*	    interception mass fluxes
!    *PDHWLS*	    soil water fluxes
!    *PDHRESS*      resistances 
!
!    *PT2M*         Screen-level air temperature  
!    *PD2M*         Screen-level air dew point temperature 
!
!     PWSAE1	 : tendency of soil water content
!     PWLE1	 : tendency of interception reservoir content
!     PSNSE1	 : tendency of snow mass

!    *PDHCO2S*      CO2 fluxes per vegetation type
!    *PDHBIOS*      biomass per vegetation type
!    *PDHVEGS*      vegetation variables per vegetation type



!     PSRSOD     : SURFACE SHORTWAVE RADIATIVE FLUX DOWNWARDS.
!     PSRTHD     : SURFACE LONGWAVE RADIATIVE FLUX DOWNWARDS.
!     PFRSO      : SHORTWAVE RADIATIVE FLUX.
!     PFRTH      : LONGWAVE RADIATIVE FLUX.
!     PALB       : MODEL SURFACE SHORTWAVE ALBEDO.
!     PFTLHEV    : SURFACE LATENT HEAT FLUX (SNOW FREE FRACTION)
!     PFTLHSB    : SURFACE LATENT HEAT FLUX (SNOW COVERED FRACTION)
!     PDIFTQ     : TURBULENT FLUX OF MOISTURE
!     PDIFTS     : TURBULENT FLUX OF ENTHALPY (OR DRY STATIC ENERGY).
!     PSTRTU     : TURBULENT MOMENTUM FLUX 
!     PSTRTV     : TURBULENT MOMENTUM FLUX 
!     PLSRF      : LARGE SCALE RAINFALL
!     PCRF       : CONVECTIVE RAINFALL
!     PLSSF      : LARGE SCALE SNOWFALL
!     PCSF       : CONVECTIVE SNOWFALL
!     PFWRO1     : SURFACE RUNOFF
!     PFWROD     : DEEP RUNOFF
!     PFWMLT     : SNOW MELT

!     PAN        : NET CO2 ASSIMILATION OVER CANOPY         
!     PAG        : GROSS CO2 ASSIMILATION OVER CANOPY   
!     PRD        : DARK RESPIRATION                         
!     PRSOIL_STR : RESPIRATION FROM SOIL AND STRUCTURAL BIOMASS
!     PRECO      : ECOSYSTEM RESPIRATION 
!     PCO2FLUX   : CO2 FLUX
!     PCH4FLUX   : CH4 FLUX
!     PBVOCFLUX  : BVOC FLUX
!     PLAI       : LAI (M2/M2)
!     PBIOM      : BIOMASS (KG/M2)
!     PBLOSS     : BIOMASS LOSS (KG/M2)
!     PBGAIN     : BIOMASS GAIN (KG/M2)
!     PBIOMSTR   : ABOVE GROUND STRUCTURAL BIOMASS (KG/M2)
!     PBIOMSTR2  : BELOW GROUND STRUCTURAL BIOMASS (KG/M2)
!     PWSAE1	 : tendency of soil water content
!     PWLE1	 : tendency of interception reservoir content
!     PSNSE1	 : tendency of snow mass
!     PTAIR      : AIR TEMPERATURE (forcing T+1, end of timestep)
!     PQAIR      : AIR SPECIFIC HUMIDITY (forcing T+1, end of timestep)
!     PUWIND     : U WIND SPEED (forcing T+1, end of timestep)
!     PVWIND     : V WIND SPEED (forcing T+1, end of timestep)



!
USE PARKIND1  ,ONLY : JPIM     ,JPRB
USE YOETHF   , ONLY : RHOH2O
USE YOESOIL1S, ONLY : RDAW
USE YOMGPD1S , ONLY : VDIEWSS  ,VDINSSS  ,VDISSHF ,GPD_SDP2, GPD_SDP3  
USE YOMGDI1S , ONLY : D1STISRD ,D1STISRU ,D1STITRD ,D1STITRU ,&
            &D1STIH   ,D1STILE  ,D1STIGFL ,D1STII   ,D1STIEVAP,&
            &D1SNFR   ,D1SNTFR  ,D1SNAFR  ,&
            &D1SRFLD  ,D1SRFLU  ,D1TRFLD  ,D1TRFLU  ,&
            &D1AHFS   ,D1AHFL   ,D1MOMU   ,D1MOMV   ,&
            &D1STNSRD ,D1STNSRU ,D1STNTRD ,D1STNTRU ,&
            &D1STNGFL ,D1STNDH  ,D1STNM   ,&
            &D1ST1SRD ,D1ST1SRU ,D1ST1TRD ,D1ST1TRU ,&
            &D1ST1H   ,D1ST1LE  ,D1STAGFL ,D1STASF  ,&
            &D1SSFL   ,D1SSFC   ,D1SWNJQ  ,D1SWNM   ,&
            &D1SWLIT  ,D1SWLJQ  ,&
            &D1SW1JBG ,D1SW1TF  ,D1SWAGFL ,D1SW1RI  ,&
            &D1SWARS  ,D1SWAEXT ,D1SW1M   ,D1SWAC   ,&
            &D1SW1C   ,D1STIFR  ,D1STITK  ,D1STIALB ,&
            &D1SVTAN  ,D1SVTAG  ,D1SVTRD  ,D1SVTRSOIL_STR,D1SVTRECO,&
            &D1SVTCO2FLUX,D1SVTRNOQ10,&
            &D1SAN    ,D1SAG    ,D1SRD    ,D1SRSOIL_STR ,D1SRECO,&
            &D1SCO2FLUX,D1SCH4FLUX, D1SBVOCFLUX, &
            &D1SLAI   ,D1SBIOM  ,D1SBLOSS ,D1SBGAIN ,D1SBIOMSTR,&
            &D1SBIOMSTRB,&
            &D1SVTLAI ,D1SVTBIOM,D1SVTBLOSS,D1SVTBGAIN,&
            &D1SVTBIOMSTR,D1SVTBIOMSTRB,&
            &D1SVTGC  ,D1SVTGA  ,D1SVTF2  ,D1SVTDS,D1SVTDMAX,&
            &D1SVTLE  ,D1SVTFR  ,D1SWFR    ,&
            &D1STAIR  ,D1SQAIR  ,D1SUWIND  ,D1SVWIND,&
            &D1SNDEPTH,D1SWAFR  ,D1SRADTK ,&
            &D1SAVTK  ,D1SVEGTK ,&
            &D1SGHF   ,D1SALB   ,&
            &D1SVEGEV ,D1SDSH   ,&
            &D1SWDS   ,&
            &D1SSDS   ,D1SWLDS  ,&
            &D1SLSRF  ,D1SCRF   ,D1SLSSF  ,D1SCSF   ,&
            &D1STE    ,D1STRO   ,D1STSRO  ,D1SMLT  ,&
            &D1SSDSSL,&
            &D1SVTRC  ,D1SVTRA ,&
            &D1T2M    ,D1D2M,&
            &D1WFLUXRF,D1HFLUXRF,D1WSN,&
            &D1STIEVAPU,D1STISKC,D1SPSURF, &
            &D1SBVOCST1,D1SBVOCST2



INTEGER(KIND=JPIM),INTENT(IN)    :: KIDIA
INTEGER(KIND=JPIM),INTENT(IN)    :: KFDIA
INTEGER(KIND=JPIM),INTENT(IN)    :: KLON 
INTEGER(KIND=JPIM),INTENT(IN)    :: KLEV
INTEGER(KIND=JPIM),INTENT(IN)    :: KLEVS
INTEGER(KIND=JPIM),INTENT(IN)    :: KLEVSN
INTEGER(KIND=JPIM),INTENT(IN)    :: KTILES 

REAL(KIND=JPRB),INTENT(IN)       :: PTSPHY

TYPE(SURF_AND_MORE_TYPE) :: PSURF
TYPE(SURF_AND_MORE_LOCAL_TYPE) :: PSURFL
TYPE(AUX_DIAG_TYPE) :: PDIAG
TYPE(AUX_DIAG_LOCAL_TYPE) :: PAUXL
TYPE(AUX_TYPE) :: PAUX
TYPE(FLUX_TYPE) :: PFLUX
TYPE(STATE_TYPE) :: STATE,TENDENCY_LOC 
TYPE(GEMS_LOCAL_TYPE) :: GEMSL 
TYPE(KEYS_LOCAL_TYPE) :: LLKEYS  
TYPE(DDH_SURF_TYPE) :: PDDHS

REAL(KIND=JPRB)   ,INTENT(IN)    :: PPSURF(KLON,NBLOCKS)
INTEGER(KIND=JPIM) :: IST,IEND,IPROMA, IBL

INTEGER(KIND=JPIM) :: JTILE,JL,jk,JLK
REAL(KIND=JPRB) :: ZEPS
REAL(KIND=JPRB) :: ZEPSILON

! balans sluitingen
INTEGER(KIND=JPIM)   :: I1

!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1) PRIVATE(IST,IEND,IBL,IPROMA)
DO IST = 1, NPOI, NPROMA
  IEND = MIN(IST+NPROMA-1,NPOI)
  IBL = (IST-1)/NPROMA + 1
  IPROMA = IEND-IST+1
GPD_SDP2(IST:IEND,:)   = PSURF%GSD%PSSDP2(1:IPROMA,:,IBL)
GPD_SDP3(IST:IEND,:,:) = PSURF%GSD%PSSDP3(1:IPROMA,:,:,IBL)

!* ==============================================================
!* ==============================================================
!*           IMPORTANT 
!* Use KIDIA,KFDIA for input array P* that are defined over KLON points 
!* Use KIDIAF:KFDIAF  for module D* arrays that are defined over all NPOINTS 
!* Be carafull when using the D* in calculations for other D* arrays 
!* ==============================================================
!* ==============================================================

ZEPS=1.E-5_JPRB
ZEPSILON=EPSILON(ZEPSILON)
!* calculations on JL 
DO JL=1,IPROMA
  D1SWDS(JL,IBL)=PTSPHY*RHOH2O*DOT_PRODUCT(PSURF%PWSAE1(JL,:,IBL),RDAW(:))
  DO JK=1,KLEVS
    D1SSDSSL(JL,JK,IBL)=PTSPHY*RHOH2O*PSURF%PWSAE1(JL,JK,IBL)*RDAW(JK)
  ENDDO
ENDDO

!*  -- no calculations just memory swap 

VDISSHF(1:IPROMA,IBL) = PFLUX%PDIFTS(1:IPROMA,KLEV,IBL)
VDIEWSS(1:IPROMA,IBL) = PFLUX%PSTRTU(1:IPROMA,KLEV,IBL)
VDINSSS(1:IPROMA,IBL) = PFLUX%PSTRTV(1:IPROMA,KLEV,IBL)


! Tile energy fluxes
D1STISRD(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,4,IBL)
D1STISRU(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,5,IBL)
D1STITRD(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,6,IBL)
D1STITRU(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,7,IBL)
D1STIH(1:IPROMA,:,IBL)     = PDDHS%PDHTLS(1:IPROMA,:,8,IBL)
D1STILE(1:IPROMA,:,IBL)    = PDDHS%PDHTLS(1:IPROMA,:,9,IBL)
D1STIGFL(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,10,IBL)
D1STII(1:IPROMA,:,IBL)     = 0.
D1STIEVAP(1:IPROMA,:,IBL)  = PDDHS%PDHTLS(1:IPROMA,:,11,IBL)
D1STISKC(1:IPROMA,:,IBL)   = PDDHS%PDHTLS(1:IPROMA,:,12,IBL)
D1STIEVAPU(1:IPROMA,:,IBL) = PSURF%GSD%PEVAPTIU(1:IPROMA,:,IBL)

! Average energy fluxes
D1SRFLD(1:IPROMA,IBL) = PSURF%PSRSOD(1:IPROMA,IBL)
D1SRFLU(1:IPROMA,IBL) = PFLUX%PFRSO(1:IPROMA,KLEV,IBL)-PSURF%PSRSOD(1:IPROMA,IBL)
D1TRFLD(1:IPROMA,IBL) = PSURF%PSRTHD(1:IPROMA,IBL)
D1TRFLU(1:IPROMA,IBL) = PFLUX%PFRTH(1:IPROMA,KLEV,IBL)-PSURF%PSRTHD(1:IPROMA,IBL)
D1AHFS(1:IPROMA,IBL)  = PFLUX%PDIFTS(1:IPROMA,KLEV,IBL)
D1AHFL(1:IPROMA,IBL)  = PFLUX%PFTLHEV(1:IPROMA,IBL)+PFLUX%PFTLHSB(1:IPROMA,IBL)
D1MOMU(1:IPROMA,IBL)  = PFLUX%PSTRTU(1:IPROMA,KLEV,IBL)
D1MOMV(1:IPROMA,IBL)  = PFLUX%PSTRTV(1:IPROMA,KLEV,IBL)
! Snow energy fluxes
D1STNSRD(1:IPROMA,IBL) = PDDHS%PDHTSS(1:IPROMA,1,7,IBL)
D1STNSRU(1:IPROMA,IBL) = PDDHS%PDHTSS(1:IPROMA,1,8,IBL)
D1STNTRD(1:IPROMA,IBL) = PDDHS%PDHTSS(1:IPROMA,1,9,IBL)
D1STNTRU(1:IPROMA,IBL) = PDDHS%PDHTSS(1:IPROMA,1,10,IBL)
D1STNGFL(1:IPROMA,IBL) = PDDHS%PDHTSS(1:IPROMA,1,13,IBL)
D1STNM(1:IPROMA,IBL)   = PDDHS%PDHTSS(1:IPROMA,1,14,IBL)
D1HFLUXRF(1:IPROMA,IBL) = 0._JPRB ! PDHTSS(1:IPROMA,1,16) Not available in surf 
! Soil energy fluxes
D1ST1SRD(1:IPROMA,IBL) = PDDHS%PDHTTS(1:IPROMA,1,5,IBL)
D1ST1SRU(1:IPROMA,IBL) = PDDHS%PDHTTS(1:IPROMA,1,6,IBL)
D1ST1TRD(1:IPROMA,IBL) = PDDHS%PDHTTS(1:IPROMA,1,7,IBL)
D1ST1TRU(1:IPROMA,IBL) = PDDHS%PDHTTS(1:IPROMA,1,8,IBL)
D1ST1H(1:IPROMA,IBL)   = PDDHS%PDHTTS(1:IPROMA,1,9,IBL)
D1ST1LE(1:IPROMA,IBL)  = PDDHS%PDHTTS(1:IPROMA,1,10,IBL)
D1STAGFL(1:IPROMA,:,IBL) = PDDHS%PDHTTS(1:IPROMA,:,11,IBL)
D1STASF(1:IPROMA,:,IBL)  = PDDHS%PDHTTS(1:IPROMA,:,12,IBL)
! Snow water fluxes
D1SSFL(1:IPROMA,IBL)  = PDDHS%PDHSSS(1:IPROMA,1,2,IBL)
D1SSFC(1:IPROMA,IBL)  = PDDHS%PDHSSS(1:IPROMA,1,3,IBL)
D1SWNJQ(1:IPROMA,IBL) = PDDHS%PDHSSS(1:IPROMA,1,4,IBL)
D1SWNM(1:IPROMA,IBL)  = PDDHS%PDHSSS(1:IPROMA,1,5,IBL)
D1WFLUXRF(1:IPROMA,IBL) = 0._JPRB ! PDHSSS(1:IPROMA,1,6) Not available in surf 
D1WSN(1:IPROMA,IBL) = 0._JPRB ! PDHSSS(1:IPROMA,1,7) Not available in surf 
! Interception water fluxes
D1SWLIT(1:IPROMA,IBL) = PDDHS%PDHIIS(1:IPROMA,2,IBL)+PDDHS%PDHIIS(1:IPROMA,3,IBL)
D1SWLJQ(1:IPROMA,IBL) = PDDHS%PDHIIS(1:IPROMA,4,IBL)
!write(6,*) 'upddiag chech interceptie balans'
!write(6,*) 'interceptie',D1SWLIT
!write(6,*) 'evaporatie',D1SWLjq
! Soil water fluxes
D1SW1JBG(1:IPROMA,IBL) = PDDHS%PDHWLS(1:IPROMA,1,9,IBL)! bare ground evaporation 
                                                   ! with mismatches from 
                                                   ! intercep & snow tiles
D1SW1TF(1:IPROMA,IBL)  = PDDHS%PDHWLS(1:IPROMA,1,3,IBL)+PDDHS%PDHWLS(1:IPROMA,1,4,IBL)
D1SWAGFL(1:IPROMA,:,IBL) = PDDHS%PDHWLS(1:IPROMA,:,8,IBL)

D1SW1RI(1:IPROMA,IBL) = PDDHS%PDHWLS(1:IPROMA,1,6,IBL) ! surface runoff
D1SWARS(1:IPROMA,:,IBL) = 0. ! excess runoff
D1SWAEXT(1:IPROMA,:,IBL) = PDDHS%PDHWLS(1:IPROMA,:,7,IBL)
D1SW1M(1:IPROMA,IBL) = PDDHS%PDHSSS(1:IPROMA,1,5,IBL)
D1SW1C(1:IPROMA,IBL) = PDDHS%PDHWLS(1:IPROMA,1,10,IBL)
D1SWAC(1:IPROMA,:,IBL) = 0. ! clipping

! Resistances for low and high vegetation type
! D1SVTRC(KIDIAF:KFDIAF,:) = PDHRESS(1:IPROMA,:,1)  PDHRESS  not used in surf...
! D1SVTRA(KIDIAF:KFDIAF,:) = PDHRESS(1:IPROMA,:,2)  PDHRESS  not used in surf...


! CO2 fluxes per vegetation type (normalized to grid)
D1SVTAG(1:IPROMA,:,IBL) =        PDDHS%PDHCO2S(1:IPROMA,:,1,IBL)
D1SVTRD(1:IPROMA,:,IBL) =        PDDHS%PDHCO2S(1:IPROMA,:,2,IBL)
D1SVTAN(1:IPROMA,:,IBL) =        PDDHS%PDHCO2S(1:IPROMA,:,3,IBL)
D1SVTRSOIL_STR(1:IPROMA,:,IBL) = PDDHS%PDHCO2S(1:IPROMA,:,4,IBL)
D1SVTRECO(1:IPROMA,:,IBL) =      PDDHS%PDHCO2S(1:IPROMA,:,5,IBL)
D1SVTCO2FLUX(1:IPROMA,:,IBL) =   PDDHS%PDHCO2S(1:IPROMA,:,6,IBL)
D1SVTRNOQ10(1:IPROMA,:,IBL) =    PDDHS%PDHCO2S(1:IPROMA,:,7,IBL)
! CO2 flux per grid
D1SAN(1:IPROMA,IBL)=PSURF%PAN(1:IPROMA,IBL)
D1SAG(1:IPROMA,IBL)=PSURF%PAG(1:IPROMA,IBL)
D1SRD(1:IPROMA,IBL)=PSURF%PRD(1:IPROMA,IBL)
D1SRSOIL_STR(1:IPROMA,IBL)=PSURF%PRSOIL_STR(1:IPROMA,IBL)
D1SRECO(1:IPROMA,IBL)     =PSURF%PRECO(1:IPROMA,IBL)
D1SCO2FLUX(1:IPROMA,IBL)  =PSURF%PCO2FLUX(1:IPROMA,IBL)
D1SCH4FLUX(1:IPROMA,IBL)  =PSURF%PCH4FLUX(1:IPROMA,IBL)
D1SBVOCFLUX(1:IPROMA,IBL) =GEMSL%PBVOCFLUX(1:IPROMA,1,IBL)

! Biomass per vegetation type
D1SVTBIOM(1:IPROMA,:,IBL)     = PDDHS%PDHBIOS(1:IPROMA,:,1,IBL)
D1SVTBLOSS(1:IPROMA,:,IBL)    = PDDHS%PDHBIOS(1:IPROMA,:,2,IBL)
D1SVTBGAIN(1:IPROMA,:,IBL)    = PDDHS%PDHBIOS(1:IPROMA,:,3,IBL) 
D1SVTBIOMSTR(1:IPROMA,:,IBL)  = PDDHS%PDHBIOS(1:IPROMA,:,4,IBL)
D1SVTBIOMSTRB(1:IPROMA,:,IBL) = PDDHS%PDHBIOS(1:IPROMA,:,5,IBL)
! Biomass per grid
D1SBIOM(1:IPROMA,IBL)    =PSURF%PBIOM(1:IPROMA,IBL)
D1SBLOSS(1:IPROMA,IBL)   =PSURF%PBLOSS(1:IPROMA,IBL)
D1SBGAIN(1:IPROMA,IBL)   =PSURF%PBGAIN(1:IPROMA,IBL)
D1SBIOMSTR(1:IPROMA,IBL) =PSURF%PBIOMSTR(1:IPROMA,IBL)
D1SBIOMSTRB(1:IPROMA,IBL)=PSURF%PBIOMSTR2(1:IPROMA,IBL)

! LAI per grid
D1SLAI(1:IPROMA,IBL)=PSURF%PLAI(1:IPROMA,IBL)

! Variables per vegetation type
D1SVTFR(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,1,IBL)
D1SVTLAI(1:IPROMA,:,IBL)  = PDDHS%PDHVEGS(1:IPROMA,:,2,IBL)
D1SVTGC(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,3,IBL)
D1SVTGA(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,4,IBL)
D1SVTF2(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,5,IBL)
D1SVTDS(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,6,IBL)
D1SVTDMAX(1:IPROMA,:,IBL) = PDDHS%PDHVEGS(1:IPROMA,:,7,IBL)
D1SVTLE(1:IPROMA,:,IBL)   = PDDHS%PDHVEGS(1:IPROMA,:,8,IBL)

! Tile values
D1STIFR(1:IPROMA,:,IBL)  = PDDHS%PDHTLS(1:IPROMA,:,1,IBL)
D1STITK(1:IPROMA,:,IBL)  = PDDHS%PDHTLS(1:IPROMA,:,2,IBL)
